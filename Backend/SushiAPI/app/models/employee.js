export class Employee {
    constructor(
        employeeId,
        name,
        birthDate,
        gender,
        joinDate,
        leaveDate,
        address,
        phone
    ) {
        this.employeeId = employeeId;
        this.name = name;
        this.birthDate = birthDate;
        this.gender = gender;
        this.joinDate = joinDate;
        this.leaveDate = leaveDate;
        this.address = address;
        this.phone = phone;
    }
}
